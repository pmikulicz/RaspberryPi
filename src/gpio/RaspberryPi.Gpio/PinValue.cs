namespace RaspberryPi.Gpio
{
    /// <summary>
    /// Defines possible pin values
    /// </summary>
    public enum PinValue
    {
        /// <summary>
        /// Represents low pin value which is logical 0
        /// </summary>
        Low = 0,

        /// <summary>
        /// Represents high pin value which is logical 1
        /// </summary>
        High = 1
    }
}